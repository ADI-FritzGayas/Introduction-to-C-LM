#include <stdio.h>

int main()
{
    // integer variable
    int age = 20;

    // floating-point variable
    float height = 5.7;

    // character variable
    char grade = 'A';

    printf("This is an exercise for variables in C\n");
    printf("---------------------------------------\n");
    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);

    return 0;
}